package com.ml.xx.bean;

public class OthersBean {
	
	private String search;
	private String id;
	
	public OthersBean() {}

	public OthersBean(String search, String id) {
		this.search = search;
		this.id = id;
	}

	public String getSearch() {
		return search;
	}

	public void setSearch(String search) {
		this.search = search;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}
	
}
